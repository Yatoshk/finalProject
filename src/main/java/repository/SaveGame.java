package repository;

public class SaveGame {
    public String fio1;
    public String fio2;
    public String sign1;
    public String sign2;
    public String moves;

    public SaveGame(String fio1, String fio2, String sign1, String sign2, String moves) {
        this.fio1 = fio1;
        this.fio2 = fio2;
        this.sign1 = sign1;
        this.sign2 = sign2;
        this.moves = moves;
    }
}
